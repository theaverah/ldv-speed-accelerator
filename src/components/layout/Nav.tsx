interface NavLinkProps {
  label: string
  href: string
  isActive: boolean
  onSetActive: () => void
  comingSoon?: boolean
}

function NavLink({ label, href, isActive, onSetActive, comingSoon }: NavLinkProps) {
  const { display, scramble } = useScramble(label)
  const [hovered, setHovered] = useState(false)

  return (
    <div style={{ position: 'relative', display: 'inline-block' }}>
      <a
        href={comingSoon ? undefined : href}
        onClick={comingSoon ? (e) => e.preventDefault() : onSetActive}
        onMouseEnter={() => { setHovered(true); if (!comingSoon) scramble() }}
        onMouseLeave={() => setHovered(false)}
        aria-current={isActive ? 'true' : undefined}
        style={{
          color: !comingSoon && (isActive || hovered) ? 'var(--color-50)' : 'var(--color-700)',
          transition: 'color 150ms',
          textDecoration: 'none',
          cursor: comingSoon ? 'default' : 'pointer',
          padding: '12px 16px',
          margin: '-12px -16px',
          display: 'inline-block',
        }}
      >
        {display}
      </a>

      {comingSoon && (
        <div
          style={{
            position: 'absolute',
            top: 'calc(100% + 8px)',
            left: '50%',
            transform: 'translateX(-50%)',
            opacity: hovered ? 1 : 0,
            transition: 'opacity 250ms ease',
            pointerEvents: 'none',
            border: '1px solid #252626',
            backgroundColor: 'var(--color-800)',
            color: 'var(--color-700)',
            padding: '8px 12px',
            fontFamily: 'var(--font-mono)',
            fontSize: '11px',
            letterSpacing: '0.1em',
            textTransform: 'uppercase',
            whiteSpace: 'nowrap',
          }}
        >
          Coming Soon!
        </div>
      )}
    </div>
  )
}

